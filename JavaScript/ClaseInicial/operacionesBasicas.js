//Operaciones Basicas
let numero1 = 15;
let numero2 = 2;

let suma = numero1 + numero2;

console.log(suma);

let resta = numero1 - numero2;

console.log(resta);

let multiplicacion = numero1 * numero2;

console.log(multiplicacion);

let division = numero1 / numero2;

console.log(division);

let modulo = numero1 % numero2;

console.log(modulo); // es dividir un numero por otro y nos da el resto de esa division

let potencia = numero1 ** numero2;

console.log(potencia); // es elevar un numero a otro numero

